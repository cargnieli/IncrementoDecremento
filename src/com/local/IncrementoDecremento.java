package com.local;

public class IncrementoDecremento {

    public static void main(String[] args) {

        int idade = 10;
        int idade1 = 10;
        int novaIdade = --idade1;
        int novaIdade1 = ++idade;


        System.out.println("Nova idade: " + novaIdade);
        System.out.println("Nova Idade1: "+ novaIdade1);

    }
}
